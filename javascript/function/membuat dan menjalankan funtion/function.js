// fungsi yg baik adalah yg fungsi yg hanaya mengerjakan 1 hal
// analogi dari padi menjadi nasi goreng membutuhkan beberap tools(function).]



function PenjumblahanDuaKubus (a,b){
    let volumeA;
    let volumeB;
    let total;

    volumeA = a * a * a;
    volumeB = b * b * b;
    total = volumeA + volumeB;

    return total;
}

alert(PenjumblahanDuaKubus(10, 5));
alert(PenjumblahanDuaKubus(300, 20000));